<h3>
	WHEN DID THE CONCEPT OF FILM AS ART START?<br />
</h3>
<p>
	In the 1870s, when Eadweard Muybridge added time and movement to the image, enabling artists to discover a new form of canvas. French and Spanish Surrealists were also fascinated by the medium. Black-and-white film-making did not cost much and gave opportunities to tap into the subconscious. Salvador Dalí had dreams of a hand crawling with ants, while film-maker Luis Buñuel ‘saw’ a cloud in the sky that sliced the moon in half. Film was the obvious medium to represent such subconscious. They collaborated on the 1929&nbsp;<em>Un Chien Andalou</em>, a work with no plot, but loaded with Freudian free association. It inspired generations of artists and film-makers. Ironically, the film attacked the bourgeois with barbed comments on moral conventions, yet was embraced by a conservative society as a new art form.&nbsp;&nbsp;
</p>
